	<p><span class="plus_icon">+</span><a class="faq" href="javascript:;">爱青大是什么，我们为什么要建立爱青大 ?</a></p>
		<div class="answer">
			<h5 class="question">爱青大是什么 ?</h5>
			<p>爱青大BBS致力于建立一个属于青海大学人的网上家园，为青海大学 在校或毕业的学生提供一个网上交流的平台，共同促进青海大学的校园氛围。</p><br />
			
			<h5 class="question">什么要建立爱青大 ?</h5>	
			<p>1、青海大学本校的BBS有局限性，出了校园网就不能访问，官方论坛学生发帖的积极性不高。</p>
			<p>2、国内许多高校都拥有学生自己的BBS系统，例如清华大学的水木社区，北京邮电大学的北邮人，兰州大学的西北旺BBS，这些论坛不仅方便了学生之间的交流，同时也让学校的情感得以加深，对于青大人互帮互助起到了渠道作用。
			
			</p>
		</div>
</li>

